using System;
using System.Collections.Generic;
using System.Text;

namespace QuantumMath
{
    public class Phase
    {
        /// <summary>
        /// Radians equivalent of 0 degrees
        /// </summary>
        public const double ZeroPI = 0D;

        /// <summary>
        /// Radians equivalent of 30 degrees
        /// </summary>
        public const double OneSixthPI = Math.PI / 6D;

        /// <summary>
        /// Radians equivalent of 45 degrees
        /// </summary>
        public const double OneForthPI = Math.PI / 4D;

        /// <summary>
        /// Radians equivalent of 60 degrees
        /// </summary>
        public const double OneThirdPI = Math.PI / 3D;

        /// <summary>
        /// Radians equivalent of 90 degrees
        /// </summary>
        public const double OneHalfPI = Math.PI / 2D;

        /// <summary>
        /// Radians equivalent of 120 degrees
        /// </summary>
        public const double TwoThirdsPI = Math.PI * (2D / 3D);

        /// <summary>
        /// Radians equivalent of 135 degrees
        /// </summary>
        public const double ThreeFourthsPI = Math.PI * (3D / 4D);

        /// <summary>
        /// Radians equivalent of 150 degrees
        /// </summary>
        public const double FiveSixthsPI = Math.PI * (5D / 6D);

        /// <summary>
        /// Radians equivalent of 180 degrees
        /// </summary>
        public const double OnePI = Math.PI;

        /// <summary>
        /// Radians equvalent of 306 degrees
        /// </summary>
        public const double TwoPI = 2 * Math.PI;

    }
}
